# AI pipeline trigger
# ---------------------------------------------------------------------------
def run_ai_pipeline(slow_query: dict, db_config: dict) -> bool:
    """
    Write the slow query to a well-known file so run_phase_1.py can pick
    it up, then execute the pipeline stages sequentially via subprocess.run().
    Returns True if all stages succeed, False otherwise.
    """
    # Write query to the handoff file read by run_phase_1.py
    try:
        ARTIFACTS_DIR.mkdir(parents=True, exist_ok=True)
        PHASE1_QUERY_FILE.write_text(json.dumps(slow_query, indent=2), encoding="utf-8")
        log_info(f"Wrote slow query to: {PHASE1_QUERY_FILE.name}")
    except OSError as exc:
        log_error(f"Failed to write query handoff file: {exc}")
        return False

    pipeline_env = {
        **os.environ,
        "PYTHONIOENCODING": "utf-8",
        "PG_HOST":     db_config.get("host", "localhost"),
        "PG_PORT":     str(db_config.get("port", 5432)),
        "PG_DBNAME":   db_config.get("dbname", "postgres"),
        "PG_USER":     db_config.get("user", "postgres"),
        "PG_PASSWORD": db_config.get("password", ""),
    }

    stages = [
        ("Phase 1 — Diagnostician",   [sys.executable, str(PHASE1_SCRIPT)]),
        ("Phase 2 — HypoPG Evaluator",[sys.executable, str(PHASE2_SCRIPT)]),
        ("Phase 3 — ORM Translator",  [sys.executable, str(PHASE3_SCRIPT)]),
    ]

    for stage_name, cmd in stages:
        log_info(f"Triggering {stage_name}...")
        if not Path(cmd[1]).exists():
            log_warn(f"Script not found, skipping: {cmd[1]}")
            continue
        try:
            result = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                encoding="utf-8",
                timeout=300,   # 5-minute per-stage timeout
                env=pipeline_env,
                cwd=str(SCRIPT_DIR),
            )
            if result.returncode != 0:
                log_error(f"{stage_name} exited with code {result.returncode}:\n{result.stderr}")
                return False
            log_info(f"{stage_name} completed successfully.")
            if result.stdout.strip():
                # Print final summary line only to keep logs clean
                last_line = result.stdout.strip().splitlines()[-1]
                log_info(f"  └─ {last_line}")
        except subprocess.TimeoutExpired:
            log_error(f"{stage_name} timed out after 300 seconds.")
            return False
        except OSError as exc:
            log_error(f"Failed to launch {stage_name}: {exc}")
            return False

    return True


