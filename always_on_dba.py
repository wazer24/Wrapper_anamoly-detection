# PostgreSQL telemetry ingestion
# ---------------------------------------------------------------------------
def fetch_slow_queries(
    db_config: dict,
    min_mean_ms: float,
    min_calls: int,
) -> list[dict]:
    """
    Query pg_stat_statements for the top 5 slowest queries that exceed
    both mean_exec_time and calls thresholds. Returns a list of row dicts.
    Requires only SELECT on pg_stat_statements (read-only access).
    """
    # pg_stat_statements column naming differs by PG version
    # PG ≥ 13: mean_exec_time; PG < 13: mean_time — handle both
    sql = """
        SELECT
            queryid,
            query,
            calls,
            mean_exec_time,
            total_exec_time,
            rows,
            stddev_exec_time,
            shared_blks_read,
            shared_blks_hit
        FROM pg_stat_statements
        WHERE
            mean_exec_time > %(min_mean_ms)s
            AND calls       > %(min_calls)s
        ORDER BY mean_exec_time DESC
        LIMIT 5;
    """

    conn = None
    try:
        conn = psycopg2.connect(**db_config)
        conn.set_session(readonly=True, autocommit=True)
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            cur.execute(sql, {"min_mean_ms": min_mean_ms, "min_calls": min_calls})
            rows = cur.fetchall()
            return [dict(r) for r in rows]
    except psycopg2.errors.UndefinedTable:
        log_warn("pg_stat_statements view not found. Enable it with: CREATE EXTENSION IF NOT EXISTS pg_stat_statements;")
        return []
    except psycopg2.OperationalError as exc:
        log_error(f"Database connection failed: {exc}")
        return []
    except psycopg2.Error as exc:
        log_error(f"PostgreSQL query error: {exc}")
        return []
    finally:
        if conn:
            try:
                conn.close()
            except psycopg2.Error:
                pass


