# Main daemon loop
# ---------------------------------------------------------------------------
def run_daemon(db_config: dict, webhook_url: str, min_mean_ms: float, min_calls: int, poll_interval: int) -> None:
    """
    The main infinite loop. Polls pg_stat_statements every `poll_interval`
    seconds, triggers the AI pipeline for new slow queries, and dispatches
    webhook alerts with the generated optimization report.
    """
    log_banner("Phase 5 — Always-On DBA Daemon Starting")
    log_info(f"Polling interval      : {poll_interval}s")
    log_info(f"Slow query threshold  : mean_exec_time > {min_mean_ms}ms")
    log_info(f"Min call threshold    : calls > {min_calls}")
    log_info(f"Cache TTL             : {CACHE_TTL_HOURS} hours")
    log_info(f"Webhook configured    : {'YES' if webhook_url else 'NO (set WEBHOOK_URL to enable)'}")
    log_info(f"Artifacts directory   : {ARTIFACTS_DIR}")
    log_info("\nDaemon is now running. Press Ctrl+C to stop.\n")

    cycle = 0

    while True:
        cycle += 1
        log_banner(f"Poll Cycle #{cycle} — {_ts()}")

        # ── 1. Ingest telemetry from pg_stat_statements ───────────────────
        log_info("Querying pg_stat_statements for slow queries...")
        try:
            slow_queries = fetch_slow_queries(db_config, min_mean_ms, min_calls)
        except Exception as exc:
            log_error(f"Unexpected error during telemetry fetch: {exc}")
            slow_queries = []

        if not slow_queries:
            log_info("No slow queries detected above threshold. System healthy. [OK]")
        else:
            log_warn(f"Found {len(slow_queries)} slow query/queries above threshold!")

            for idx, row in enumerate(slow_queries, start=1):
                query_text = row.get("query", "")
                queryid    = row.get("queryid")
                mean_ms    = row.get("mean_exec_time", 0.0)
                calls      = row.get("calls", 0)

                log_warn(
                    f"  [{idx}] queryid={queryid} | mean={mean_ms:.1f}ms | calls={calls:,}"
                )

                # ── 2. Deduplication check ────────────────────────────────
                cache_key = _make_cache_key(queryid, query_text)
                if _is_cached(cache_key):
                    log_info(f"  [{idx}] Already optimized within {CACHE_TTL_HOURS}h cache window — skipping.")
                    continue

                log_info(f"  [{idx}] New slow query detected — triggering AI optimization pipeline...")

                # ── 3. Trigger AI pipeline ────────────────────────────────
                try:
                    rows_scanned_proxy = row.get("shared_blks_read", 0) + row.get("shared_blks_hit", 0)
                    payload = {
                        "raw_query": query_text,
                        "duration_ms": mean_ms,
                        "rows_returned": row.get("rows", 0),
                        "rows_scanned": rows_scanned_proxy,
                        "payload_size_kb": None
                    }
                    success = run_ai_pipeline(payload, db_config)
                except Exception as exc:
                    log_error(f"  [{idx}] Unhandled error in AI pipeline: {exc}")
                    success = False

                if not success:
                    log_error(f"  [{idx}] AI pipeline failed. Skipping alert for this query.")
                    continue

                # Mark in cache AFTER successful pipeline run
                _mark_cached(cache_key)
                log_info(f"  [{idx}] Query added to 24h deduplication cache.")

                # ── 4. Read optimization report ───────────────────────────
                report_md = ""
                try:
                    if PRISMA_MD_FILE.exists() and PRISMA_MD_FILE.stat().st_size > 0:
                        report_md = PRISMA_MD_FILE.read_text(encoding="utf-8").strip()
                        log_info(f"  [{idx}] Read optimization report: {len(report_md)} chars.")
                    else:
                        log_warn(f"  [{idx}] PRISMA_INSTRUCTIONS.md not found or empty after pipeline run.")
                except OSError as exc:
                    log_error(f"  [{idx}] Could not read report file: {exc}")

                # ── 5. Send webhook alert ─────────────────────────────────
                try:
                    send_webhook_alert(webhook_url, row, report_md)
                except Exception as exc:
                    log_error(f"  [{idx}] Unexpected error sending webhook alert: {exc}")

        # ── Sleep before next poll ────────────────────────────────────────
        log_info(f"Next poll in {poll_interval}s. Sleeping...\n")
        try:
            time.sleep(poll_interval)
        except KeyboardInterrupt:
            log_banner("Daemon Interrupted by User")
            log_info("Shutting down cleanly. All virtual indexes cleaned up.")
            sys.exit(0)


