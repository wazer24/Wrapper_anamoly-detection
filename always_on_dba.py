# ---------------------------------------------------------------------------
# Deduplication cache helpers
# ---------------------------------------------------------------------------
def _make_cache_key(queryid: int | None, query_text: str) -> str:
    """Create a stable hash key for a query, using queryid if available."""
    raw = str(queryid) if queryid else query_text[:512]
    return hashlib.sha256(raw.encode("utf-8")).hexdigest()


def _is_cached(cache_key: str) -> bool:
    """Return True if this query was optimized within the TTL window."""
    if cache_key not in _optimized_cache:
        return False
    age = datetime.now() - _optimized_cache[cache_key]
    if age > timedelta(hours=CACHE_TTL_HOURS):
        del _optimized_cache[cache_key]
        return False
    return True


def _mark_cached(cache_key: str) -> None:
    """Record that we've dispatched this query to the optimizer."""
    _optimized_cache[cache_key] = datetime.now()


