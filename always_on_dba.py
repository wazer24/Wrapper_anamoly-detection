def _build_discord_payload(slow_query_row: dict, report_md: str) -> dict:
    """Format a Discord webhook payload with embeds."""
    query_preview  = slow_query_row["query"][:1000].replace("\n", " ")
    mean_ms        = slow_query_row.get("mean_exec_time", 0)
    calls          = slow_query_row.get("calls", 0)
    report_preview = report_md[:2000] if report_md else "_No report generated._"

    return {
        "content": "⚠️ **Always-On DBA Alert — Slow Query Detected & Optimized**",
        "embeds": [
            {
                "title": "Slow Query Report",
                "color": 16711680,  # Red
                "fields": [
                    {"name": "Mean Exec Time",   "value": f"`{mean_ms:.1f} ms`", "inline": True},
                    {"name": "Total Calls",      "value": f"`{calls:,}`",         "inline": True},
                    {"name": "Slow Query",       "value": f"```sql\n{query_preview}\n```", "inline": False},
                    {"name": "AI Optimization Report", "value": report_preview[:1024], "inline": False},
                ],
                "footer": {"text": f"Always-On DBA Daemon · {_ts()}"}
            }
        ]
    }


def send_webhook_alert(webhook_url: str, slow_query_row: dict, report_md: str) -> None:
    """
    POST a formatted alert to a Slack or Discord webhook.
    Automatically detects the platform from the URL structure.
    """
    if not webhook_url:
        log_warn("No WEBHOOK_URL configured — skipping alert notification.")
        return

    is_discord = "discord.com" in webhook_url or "discordapp.com" in webhook_url

    try:
        if is_discord:
            payload = _build_discord_payload(slow_query_row, report_md)
        else:
            payload = _build_slack_payload(slow_query_row, report_md)

        headers = {"Content-Type": "application/json"}
        resp = requests.post(
            webhook_url,
            data=json.dumps(payload),
            headers=headers,
            timeout=15,
        )
        resp.raise_for_status()
        log_info(f"Webhook alert sent successfully (HTTP {resp.status_code}).")
    except requests.exceptions.Timeout:
        log_error("Webhook request timed out after 15 seconds.")
    except requests.exceptions.ConnectionError as exc:
        log_error(f"Webhook connection failed: {exc}")
    except requests.exceptions.HTTPError as exc:
        log_error(f"Webhook returned error: {exc.response.status_code} — {exc.response.text[:200]}")
    except requests.exceptions.RequestException as exc:
        log_error(f"Unexpected webhook error: {exc}")


